#!/bin/bash

# Скрипт запуска анализатора коммерческих предложений

set -e

echo "========================================="
echo "Запуск анализатора коммерческих предложений"
echo "========================================="

# Проверка виртуального окружения
if [ ! -d "venv" ]; then
    echo "❌ Виртуальное окружение не найдено."
    echo "Запустите сначала setup.sh для настройки"
    exit 1
fi

# Активация виртуального окружения
source venv/bin/activate

# Проверка зависимостей
if ! python -c "import streamlit" &> /dev/null; then
    echo "❌ Зависимости не установлены."
    echo "Запустите сначала setup.sh для установки"
    exit 1
fi

echo "✅ Зависимости проверены"
echo "🚀 Запуск Streamlit приложения..."

streamlit run main.py